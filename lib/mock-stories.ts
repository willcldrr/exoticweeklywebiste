export function getStoryBySlug(slug: string): Story | undefined {
  return mockStories.find(story => story.slug === slug);
}

export function getStoriesByCategory(category: string): Story[] {
  return mockStories.filter(story => story.category === category && story.status === 'published');
}

export function getFeaturedStories(): Story[] {
  return mockStories.filter(story => story.featured && story.status === 'published');
}

export function getPublishedStories(): Story[] {
  return mockStories.filter(story => story.status === 'published');
}

export function getLatestStories(count: number = 6): Story[] {
  return getPublishedStories()
    .sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime())
    .slice(0, count);
}
